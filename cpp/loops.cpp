#include <iostream>

using namespace std;

int main(){
    int n;
    cout << "Digite um número inteiro não negativo\n";
    cin >> n;


    int soma = 0;
    int cont = 1;
    while(cont <= soma){
        soma +=cont;
        
        break
    ;}

    for(int j = 1; j<=n; j+=1){
        soma += j;
    }

    do
    {
        soma +=cont;
        cont++;
    } while (cont <= n);
    
} 